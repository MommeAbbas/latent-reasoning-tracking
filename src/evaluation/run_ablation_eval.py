"""
Ablation study: compares RBPF variants against PF and EKF baselines.

Each variant uses a different internal filter model while the true data is
always generated by the full SLDSDynamics.  This isolates the contribution
of each modelling component.

Variants
--------
  RBPF-Full       RBPF with correct full dynamics (rotation + all modes)
  RBPF-NoRot      RBPF whose internal model has no rotation (misspecified)
  RBPF-2Mode      RBPF whose internal model has only NORMAL + INSIGHT modes
  PF              Standard particle filter (full dynamics, no Rao-Blackwell)
  EKF             Extended Kalman Filter (continuous-only, no modes)
  LR-k5           Logistic regression on first 5 steps (supervised)

Output
------
  tables/ablation_results.csv
"""

import os
import gc
import csv
import numpy as np

from src.simulation.slds import (
    SLDSConfig, SLDSDynamics, SLDSSimulator, Mode,
    make_no_rotation_dynamics, make_two_mode_dynamics,
)
from src.simulation.sensors import SensorConfig, ReasoningSensors
from src.filters.rbpf_slds  import RBPF_SLDS, RBPFConfig
from src.filters.ekf_baseline import EKFBaseline, EKFConfig
from src.filters.pf_baseline  import PF_SLDS, PFConfig
from src.filters.lr_baseline  import LRBaseline
from src.evaluation.online_prediction import OnlineCorrectnessPredictor, OnlinePredictionRecorder
from src.evaluation.run_simulation_eval import (
    make_dynamics_and_sensors, compute_label,
)

# ── metric helper ─────────────────────────────────────────────────────────────
try:
    from sklearn.metrics import roc_auc_score
except ImportError:
    roc_auc_score = None


def safe_auc(labels, scores):
    if roc_auc_score is None or len(np.unique(labels)) < 2:
        return float("nan")
    return float(roc_auc_score(labels, scores))


# ── constants ─────────────────────────────────────────────────────────────────
N_SEEDS  = 5
N_TRAJ   = 200
T        = 35
PARTICLES = 125
EARLY_K  = 4        # 0-indexed


# ── per-seed evaluation ───────────────────────────────────────────────────────
def run_ablation_seed(seed: int, true_dyn, sensors, predictor):
    """
    Generate N_TRAJ trajectories with `true_dyn`, then evaluate all variants.
    Returns dict: variant_name -> {auc_final, auc_early}.
    """
    np.random.seed(seed)

    # ── prepare filter-internal dynamics variants ─────────────────────────────
    true_cfg = true_dyn.cfg

    no_rot_dyn  = make_no_rotation_dynamics(true_cfg)
    two_mode_dyn = make_two_mode_dynamics(true_cfg)

    # ── collect trajectories ──────────────────────────────────────────────────
    preds = {name: [] for name in [
        "RBPF-Full", "RBPF-NoRot", "RBPF-2Mode", "PF", "EKF"
    ]}
    labels_list = []
    all_ys = []       # for LR

    for n in range(N_TRAJ):
        xs, zs = SLDSSimulator(true_dyn).run(T=T)
        label  = compute_label(xs, zs)
        labels_list.append(label)

        # collect raw observations for LR
        ys = np.array([sensors.observe(xs[k + 1]) for k in range(T)])
        all_ys.append(ys)

        # instantiate filters
        rbpf_full    = RBPF_SLDS(dyn=true_dyn,    sensors=sensors, cfg=RBPFConfig(num_particles=PARTICLES))
        rbpf_norot   = RBPF_SLDS(dyn=no_rot_dyn,  sensors=sensors, cfg=RBPFConfig(num_particles=PARTICLES))
        rbpf_2mode   = RBPF_SLDS(dyn=two_mode_dyn, sensors=sensors, cfg=RBPFConfig(num_particles=PARTICLES))
        pf           = PF_SLDS(dyn=true_dyn,       sensors=sensors, cfg=PFConfig(num_particles=PARTICLES))
        ekf          = EKFBaseline(dyn=true_dyn,   sensors=sensors, cfg=EKFConfig())

        rec_full  = OnlinePredictionRecorder()
        rec_norot = OnlinePredictionRecorder()
        rec_2mode = OnlinePredictionRecorder()
        rec_pf    = OnlinePredictionRecorder()
        rec_ekf   = OnlinePredictionRecorder()

        for k in range(T):
            y = sensors.observe(xs[k + 1])

            rbpf_full.step(y)
            rec_full.update(predictor.prob_correct_from_particles(rbpf_full.mus, rbpf_full.weights))

            rbpf_norot.step(y)
            rec_norot.update(predictor.prob_correct_from_particles(rbpf_norot.mus, rbpf_norot.weights))

            rbpf_2mode.step(y)
            rec_2mode.update(predictor.prob_correct_from_particles(rbpf_2mode.mus, rbpf_2mode.weights))

            x_hat_pf, _ = pf.step(y)
            rec_pf.update(predictor.prob_correct_from_state(x_hat_pf))

            mu, _ = ekf.step(y)
            rec_ekf.update(predictor.prob_correct_from_state(mu))

        preds["RBPF-Full"].append(rec_full.as_array())
        preds["RBPF-NoRot"].append(rec_norot.as_array())
        preds["RBPF-2Mode"].append(rec_2mode.as_array())
        preds["PF"].append(rec_pf.as_array())
        preds["EKF"].append(rec_ekf.as_array())

        del rbpf_full, rbpf_norot, rbpf_2mode, pf, ekf
        if n % 40 == 0:
            gc.collect()

    labels = np.array(labels_list)

    # ── compute AUC per variant ───────────────────────────────────────────────
    results = {}
    for name, pred_list in preds.items():
        arr = np.array(pred_list)          # (N_TRAJ, T)
        results[name] = {
            "auc_final": safe_auc(labels, arr[:, -1]),
            "auc_early": safe_auc(labels, arr[:, EARLY_K]),
        }

    # ── LR baseline (supervised, 70/30 split) ────────────────────────────────
    n_train = int(0.70 * N_TRAJ)
    lr = LRBaseline(k=EARLY_K + 1)   # use same k as early-step AUC
    lr.fit(all_ys[:n_train], labels[:n_train])
    lr_probs = lr.predict(all_ys[n_train:])
    results["LR-k5"] = {
        "auc_final": safe_auc(labels[n_train:], lr_probs),
        "auc_early": safe_auc(labels[n_train:], lr_probs),
    }

    return results


# ── main ──────────────────────────────────────────────────────────────────────
def main():
    true_dyn, sensors = make_dynamics_and_sensors()
    predictor = OnlineCorrectnessPredictor()

    variant_names = ["RBPF-Full", "RBPF-NoRot", "RBPF-2Mode", "PF", "EKF", "LR-k5"]
    all_results = {name: {"auc_final": [], "auc_early": []} for name in variant_names}

    for seed in range(N_SEEDS):
        print(f"[ablation] Seed {seed+1}/{N_SEEDS} ...")
        seed_results = run_ablation_seed(seed, true_dyn, sensors, predictor)
        for name in variant_names:
            if name in seed_results:
                all_results[name]["auc_final"].append(seed_results[name]["auc_final"])
                all_results[name]["auc_early"].append(seed_results[name]["auc_early"])

    # ── print table ───────────────────────────────────────────────────────────
    print("\n=== Ablation Results (mean ± std over 5 seeds) ===")
    print(f"{'Method':<16} {'AUC@early':>12} {'AUC@final':>12}")
    print("-" * 42)
    for name in variant_names:
        ef = np.array(all_results[name]["auc_early"])
        ff = np.array(all_results[name]["auc_final"])
        print(f"{name:<16} {ef.mean():.3f}±{ef.std():.3f}   {ff.mean():.3f}±{ff.std():.3f}")

    # ── save CSV ──────────────────────────────────────────────────────────────
    os.makedirs("tables", exist_ok=True)
    csv_path = os.path.join("tables", "ablation_results.csv")
    with open(csv_path, "w", newline="") as f:
        writer = csv.writer(f)
        writer.writerow(["method", "seed", "auc_final", "auc_early"])
        for name in variant_names:
            for s, (af, ae) in enumerate(
                zip(all_results[name]["auc_final"], all_results[name]["auc_early"])
            ):
                writer.writerow([name, s, f"{af:.4f}", f"{ae:.4f}"])
    print(f"\nSaved to {csv_path}")


if __name__ == "__main__":
    main()
